# ---------------------------------------------------------------------------
# Helper: JPEG with EXIF
# ---------------------------------------------------------------------------

def _make_jpeg_with_exif(exif_dict: dict) -> bytes:
    img = Image.new("RGB", (32, 32), color=(128, 128, 128))
    buf = io.BytesIO()
    img.save(buf, format="JPEG", exif=piexif.dump(exif_dict))
    return buf.getvalue()


def _read_exif(jpeg_bytes: bytes) -> dict:
    return piexif.load(jpeg_bytes)


