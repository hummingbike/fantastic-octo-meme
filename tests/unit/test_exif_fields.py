    def test_maker_note_round_trip(self):
        """MakerNote 필드 (도구 고유 바이너리) 쓰기·읽기가 성공해야 한다."""
        maker_note = b"\x00\x01\x02TOOL_SPECIFIC_AI_FLAG"
