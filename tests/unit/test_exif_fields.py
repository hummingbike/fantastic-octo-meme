class TestUserCommentDecoding:
    """UserComment 바이트 디코딩 로직 검증."""

    def _decode_user_comment(self, raw: bytes) -> str | None:
        """ASCII/Unicode prefix 를 제거하고 사람이 읽는 문자열을 반환한다."""
        if not raw:
            return None
        prefixes = {
            b"ASCII\x00\x00\x00": "ascii",
            b"UNICODE\x00": "utf-16-le",
            b"JIS\x00\x00\x00\x00\x00": "iso-2022-jp",
        }
        for prefix, enc in prefixes.items():
            if raw.startswith(prefix):
                payload = raw[len(prefix):]
                try:
                    return payload.decode(enc).rstrip("\x00").strip()
                except UnicodeDecodeError:
                    return None
        # prefix 없음 — 그대로 latin-1 시도
        try:
            return raw.decode("latin-1").strip()
        except Exception:
            return None

    def test_ascii_prefix_decoded(self):
        raw = b"ASCII\x00\x00\x00AI Generated"
        result = self._decode_user_comment(raw)
        assert result == "AI Generated"

    def test_unicode_prefix_decoded(self):
        text = "AI 생성"
        raw = b"UNICODE\x00" + text.encode("utf-16-le")
        result = self._decode_user_comment(raw)
        assert result == text

    def test_empty_returns_none(self):
        assert self._decode_user_comment(b"") is None

    def test_no_prefix_fallback(self):
        raw = b"AI Generated"
        result = self._decode_user_comment(raw)
        assert result is not None
        assert "AI" in result


class TestAiKeywordDetection:
    """UserComment 에서 AI 키워드 탐지 로직 검증."""

    def _contains_ai_keyword(self, text: str) -> bool:
        lower = text.lower()
        keywords = [k.lower() for k in USER_COMMENT_AI_KEYWORDS]
        return any(k in lower for k in keywords)

    def test_en_ai_generated_detected(self):
        assert self._contains_ai_keyword("AI Generated")

    def test_en_aigc_detected(self):
        assert self._contains_ai_keyword("AIGC content")

    def test_ko_ai_generated_detected(self):
        assert self._contains_ai_keyword("이 이미지는 AI 생성 콘텐츠입니다")

    def test_no_ai_keyword_returns_false(self):
        assert not self._contains_ai_keyword("Photo taken at Seoul")

    def test_case_insensitive(self):
        assert self._contains_ai_keyword("ai generated")
        assert self._contains_ai_keyword("AI GENERATED")


class TestKnownToolExifPatterns:
    """실제 AI 도구별 EXIF 패턴 문서화 및 검증."""

